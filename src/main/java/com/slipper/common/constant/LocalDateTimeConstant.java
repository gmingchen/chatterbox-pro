package com.slipper.common.constant;

/**
 * 日期常量
 * @author gumingchen
 */
public class LocalDateTimeConstant {
    /**
     * 例：2023-12-01 10:02:09
     */
    public static final String DATETIME_PATTERN = "yyyy-MM-dd HH:mm:ss";
    /**
     * 例：2023-12-01
     */
    public static final String DATE_PATTERN = "yyyy-MM-dd";
    /**
     * 例：10:02:09
     */
    public static final String TIME_PATTERN = "HH:mm:ss";
    /**
     * 例：10月02日
     */
    public static final String MONTH_DAY_PATTERN = "MM月dd日";
    /**
     * 例：20231201100209
     */
    public static final String DATETIME_NUMBER_PATTERN = "yyyyMMddHHmmss";
}
